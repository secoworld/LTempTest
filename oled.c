//写入数据
void LCD_WrDat(uchar dat)
{
	uchar i=0;
	CS_0;
	DC_1;
	SCL_0;
	
	for(i=0;i<8;i++)
	{
		if(dat&0x80)
		{
			SDA_1;
		}
		else
		{
			SDA_0;
		}
		SCL_1;
		;;;
		SCL_0;
		dat<<=1;
	}
	
	CS_1;
}

//写入命令
void LCD_WrCmd(uchar dat)
{
	uchar i=0;
	CS_0;
	DC_0;
	SCL_0;
	
	for(i=0;i<8;i++)
	{
		if(dat&0x80)
		{
			SDA_1;
		}
		else
		{
			SDA_0;
		}
		SCL_1;
		;;;
		SCL_0;
		dat<<=1;
	}
	
	CS_1;
}

//设置坐标
void LCD_Set_Pos(uchar x,uchar y)
{
  LCD_WrCmd(0xb0+y);
  LCD_WrCmd(((x&0xf0)>>4)|0x10);
  LCD_WrCmd((x&0x0f)|0x00); 
}

//填充屏幕
void LCD_Fill(unsigned char bmp_dat)
{
	unsigned char y,x;
	
	for(y=0;y<8;y++)
	{
		LCD_WrCmd(0xb0+y);
		LCD_WrCmd(0x01);
		LCD_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
			LCD_WrDat(bmp_dat);
		
	}
	
}

//将OLED_Data填充
void OLED_Fill(unsigned char bmp_dat)
{
	unsigned char y,x;
	
	for(y=0;y<8;y++)
	{
		LCD_WrCmd(0xb0+y);
		LCD_WrCmd(0x01);
		LCD_WrCmd(0x10);
		for(x=0;x<X_WIDTH;x++)
			OLED_Data[x][y]=bmp_dat;
	}	
	
}

//清屏
void LCD_CLS(void)
{
	unsigned char y,x;	
	for(y=0;y<8;y++)
	{
		LCD_WrCmd(0xb0+y);
		LCD_WrCmd(0x01);
		LCD_WrCmd(0x10); 
		for(x=0;x<X_WIDTH;x++)
			LCD_WrDat(0);
	}
}

//将OLED_Data清屏
void OLED_CLS(void)
{
	uchar x,y;
	for(y=0;y<8;y++)
	{
		for(x=0;x<X_WIDTH;x++)
		{
			OLED_Data[x][y]=0;
		}
	}
}

//OLED初始化
void LCD_Init(void)        
{  
  //LCD_SCL=1;
  digitalWrite(SCL,1);
  //LCD_CS=1; //预制SLK和SS为高电平
  digitalWrite(CS,1);    
  //LCD_RST=0;
  digitalWrite(RST,0);
  delay(50);
 // LCD_RST=1;
  digitalWrite(RST,1);
  
  //从上电到下面开始初始化要有足够的时间，即等待RC复位完毕   
  
  LCD_WrCmd(0xae);//--turn off oled panel
  LCD_WrCmd(0x00);//---set low column address
  LCD_WrCmd(0x10);//---set high column address
  LCD_WrCmd(0x40);//--set start line address  Set Mapping RAM Display Start Line (0x00~0x3F)
  LCD_WrCmd(0x81);//--set contrast control register
  LCD_WrCmd(0xcf); // Set SEG Output Current Brightness
  LCD_WrCmd(0xa1);//--Set SEG/Column Mapping     0xa0左右反置 0xa1正常
  LCD_WrCmd(0xc8);//Set COM/Row Scan Direction   0xc0上下反置 0xc8正常
  LCD_WrCmd(0xa6);//--set normal display
  LCD_WrCmd(0xa8);//--set multiplex ratio(1 to 64)
  LCD_WrCmd(0x3f);//--1/64 duty
  LCD_WrCmd(0xd3);//-set display offset Shift Mapping RAM Counter (0x00~0x3F)
  LCD_WrCmd(0x00);//-not offset
  LCD_WrCmd(0xd5);//--set display clock divide ratio/oscillator frequency
  LCD_WrCmd(0x80);//--set divide ratio, Set Clock as 100 Frames/Sec
  LCD_WrCmd(0xd9);//--set pre-charge period
  LCD_WrCmd(0xf1);//Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
  LCD_WrCmd(0xda);//--set com pins hardware configuration
  LCD_WrCmd(0x12);
  LCD_WrCmd(0xdb);//--set vcomh
  LCD_WrCmd(0x40);//Set VCOM Deselect Level
  LCD_WrCmd(0x20);//-Set Page Addressing Mode (0x00/0x01/0x02)
  LCD_WrCmd(0x02);//
  LCD_WrCmd(0x8d);//--set Charge Pump enable/disable
  LCD_WrCmd(0x14);//--set(0x10) disable
  LCD_WrCmd(0xa4);// Disable Entire Display On (0xa4/0xa5)
  LCD_WrCmd(0xa6);// Disable Inverse Display On (0xa6/a7) 
  LCD_WrCmd(0xaf);//--turn on oled panel
  LCD_Fill(0x00);  //初始清屏
  LCD_Set_Pos(0,0);   
} 

//写入6*8的字符
void LCD_P6x8Str(unsigned char x,unsigned char y,char *ch)
{
  unsigned char c=0,i=0;      
  while (*ch!='\0')
  {    
    c =*ch-32;
    if(x>126){x=0;y++;}
    LCD_Set_Pos(x,y);    
    for(i=0;i<6;i++)     
      LCD_WrDat(F6x8[c][i]);  
    x+=6;
    ch++;
  }
}


//向OLED_Data中写入6*8字符
void OLED_P6X8Str(uchar x,uchar y,char *ch)
{
	uchar  c=0,i=0;
	while((*ch)!='\0')
	{
		c= (*ch)-32;
		if(x>126)
		{
			x=0;y++;
		}
		for(i=0;i<6;i++)
		{
			OLED_Data[x][y]=F6x8[c][i];	
			x++;
		}
		//x+=6;
		ch++; 
	}
	
	
}


//将OLED_Data[][]中的内容写入屏幕
void OLED_Write()
{
	uchar x,y;
	for(y=0;y<8;y++)
	{
		LCD_WrCmd(0xb0+y);
		LCD_WrCmd(0x01);
		LCD_WrCmd(0x10); 
		for(x=0;x<X_WIDTH;x++)
			LCD_WrDat(OLED_Data[x][y]);
	}
}
